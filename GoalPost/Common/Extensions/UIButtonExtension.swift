import UIKit

extension UIButton {
    func setSelectedColor() {
        backgroundColor = #colorLiteral(red: 0.4928511977, green: 0.7733766437, blue: 0.4621263146, alpha: 1)
    }
    
    func setDeselectedColor() {
        backgroundColor = #colorLiteral(red: 0.6519300938, green: 0.8728946447, blue: 0.6689990759, alpha: 1)
    }
}
